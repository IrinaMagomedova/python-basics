'''
3. Узнайте у пользователя число n. Найдите сумму чисел n + nn + nnn.
Например, пользователь ввёл число 3.
 Считаем 3 + 33 + 333 = 369.
'''

n = input("Введите целое число: ")
num1 = int(n)
num2 = int(f'{n}{n}')
num3 = int(f'{n}{n}{n}')
sum_n = num1 + num2 + num3
print(f'{num1}+{num2}+{num3} = {sum_n}')
